package day04_concatenation;

public class primitiveTypeCasting {
    public static void main(String[] args) {
        //------Implicit casting-----smaller to larger and is done automatic
        byte a = 15;
        short b = a;
        int c = a; //implicit casting
        long d = 3000L;

        float f = d;
        //===explicit casting
        double k = 52565854;
        int e = (int) k;
        byte y = (byte)e; // explicit casting
        float z = 20.8f;
        short q = (short)z; // z = 20

        double n1 = 2.5;
        byte n2 = (byte)n1; //explicit casting









    }
}
