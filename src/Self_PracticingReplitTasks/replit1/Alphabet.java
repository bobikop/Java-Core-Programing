package Self_PracticingReplitTasks.replit1;

public class Alphabet {

    public static void main(String[] args) {
        for (char i = 'A'; i <= 'Z'; i++) {

            System.out.print(i + " ");
        }
        System.out.println();

        for (char i = 'z'; i >= 'a'; i--) {

            System.out.print(i + " ");
        }
    }
}
