public class Has55 {
    public static void main(String[] args) {

        int[] nums = {1, 5, 5, 1, 1};

        String result = "";

        for (int i = 0,j = i + 1; i < nums.length-1; i++, j++) {
            if(nums[i] == 5 && nums[j] == 5);
            result += nums[i] + " ";
            System.out.println(result);
        }






    }
}
