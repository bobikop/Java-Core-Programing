package day02_escapeSequences;

public class EscapeSequences {
    public static void main(String[] args) {

        System.out.println("Hello Students \nToday we have Java Class"); // \n this sequence adds a new line
        System.out.println("----------------------------------------");
        System.out.println("\tJava Programming Language");// \t this sequence add paragraph space
        System.out.println("\t\t\tJava Developer Course Batch 1");// multiple \t add multiple space (paragraph)
        System.out.println("\\");// \\ ths print backslash
        System.out.println("My favorite TV show is \"Narcos\"");// \" this sequence prints double quote only!


    }
}
/*
 1. \n : New Line
 2. \t : Paragraph space
 3. \\ : Single back slash
 4. \" : Double quote
 */