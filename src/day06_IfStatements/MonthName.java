package day06_IfStatements;

public class MonthName {
    public static void main(String[] args) {
        int number = 8;
        String monthName = "";

        if (number == 1) {
            monthName = "January";
        }
        if (number == 2) {
            monthName = "February";
        }
        if (number == 3) {
            monthName = "March";
        }
        if (number == 4) {
            monthName = "April";
        }
        if (number == 5) {
            monthName = "May";
        }
        if (number == 6) {
            monthName = "June";
        }
        if (number == 7) {
            monthName = "July";
        }
        if (number == 8) {
            monthName = "August";
        }
        if (number == 9) {
            monthName = "September";
        }
        if (number == 10) {
            monthName = "October";
        }
        if (number == 11) {
            monthName = "November";
        }
        if (number == 12) {
            monthName = "December";
        }
        System.out.println(monthName);

    }
}