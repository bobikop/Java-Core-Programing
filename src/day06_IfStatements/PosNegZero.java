package day06_IfStatements;

public class PosNegZero {
    public static void main(String[] args) {
        int num = 52;

        if(num > 0){
            System.out.println(num + " is positive number ");
        }
        if(num < 0){
            System.out.println(num + " is negative number");
        }
        if(num == 0){
            System.out.println(num + " is zero");
        }

    }
}
